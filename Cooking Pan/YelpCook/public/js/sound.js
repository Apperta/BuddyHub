function enableMute() { 
    var tempAudioGen = "tempAudio";
    for (var i=0;i<10;i++){
        tempAudioGen += i.toString();
        tempAudioGen.muted = true;
    }
    
} 

function disableMute() { 
    var tempAudioGen = "tempAudio";
    for (var i=0;i<10;i++){
        tempAudioGen += i.toString();
        tempAudioGen.muted = true;
    }
} 

